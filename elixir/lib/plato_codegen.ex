defmodule PlatoCodegen.CLI do
  @moduledoc """
  Entry point for the plato_codegen escript.

  Usage:
      ./plato_codegen [manifest_path] [output_path]

  Defaults:
      manifest_path -> plato-manifest.json
      output_path   -> plato_client.ex
  """

  def main(args) do
    {manifest_path, output_path} =
      case args do
        [] -> {"plato-manifest.json", "plato_client.ex"}
        [m] -> {m, "plato_client.ex"}
        [m, o | _] -> {m, o}
      end

    with {:read, {:ok, raw}} <- {:read, File.read(manifest_path)},
         {:parse, {:ok, manifest}} <- {:parse, parse_json(raw)},
         {:gen, source} <- {:gen, PlatoCodegen.generate(manifest)},
         {:write, :ok} <- {:write, File.write(output_path, source)} do
      IO.puts("Generated #{output_path} from #{manifest_path}")
    else
      {:read, {:error, reason}} ->
        IO.puts(:stderr, "Error reading manifest '#{manifest_path}': #{:file.format_error(reason)}")
        System.halt(1)

      {:parse, {:error, reason}} ->
        IO.puts(:stderr, "Error parsing JSON in '#{manifest_path}': #{inspect(reason)}")
        System.halt(1)

      {:write, {:error, reason}} ->
        IO.puts(:stderr, "Error writing '#{output_path}': #{:file.format_error(reason)}")
        System.halt(1)
    end
  end

  # ---------------------------------------------------------------------------
  # JSON parsing — uses :json (OTP 27+)
  # ---------------------------------------------------------------------------

  defp parse_json(raw) when is_binary(raw) do
    try do
      decoded = :json.decode(raw)
      {:ok, atomise_keys(decoded)}
    rescue
      e -> {:error, Exception.message(e)}
    catch
      kind, value -> {:error, {kind, value}}
    end
  end

  # Recursively convert map string keys to atoms (safe — keys come from a
  # controlled schema document, not arbitrary user input).
  defp atomise_keys(map) when is_map(map) do
    Map.new(map, fn {k, v} -> {String.to_atom(k), atomise_keys(v)} end)
  end

  defp atomise_keys(list) when is_list(list), do: Enum.map(list, &atomise_keys/1)
  defp atomise_keys(value), do: value
end

